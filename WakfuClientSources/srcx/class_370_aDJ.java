public class aDJ
{
  private final bnn dYM;
  private final dxL bXQ;

  public aDJ(bnn parambnn, dxL paramdxL)
  {
    this.dYM = parambnn;
    this.bXQ = new cYq(paramdxL);
  }

  public float aPU() {
    return this.dYM.oy();
  }

  public float aPV() {
    return this.dYM.aIQ();
  }

  public float aPW() {
    return this.dYM.bxz();
  }

  public dxL VN() {
    return this.bXQ;
  }

  public bnn aPX() {
    return this.dYM;
  }
}