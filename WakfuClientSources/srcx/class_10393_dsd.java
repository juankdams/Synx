public class dsd
{
  private final bNa bPo;
  private final bkm luv;

  dsd(bNa parambNa, bkm parambkm)
  {
    this.bPo = parambNa;
    this.luv = parambkm;
  }

  public boolean cYM() {
    return this.bPo != null;
  }

  public bNa getColor()
  {
    return this.bPo;
  }

  public boolean cYN() {
    return this.luv != null;
  }

  public bkm cYO()
  {
    return this.luv;
  }
}